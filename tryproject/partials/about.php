        <h1 class="h1">Blood Banking</h1>
        <h2 class="h2">What is blood banking?</h2>
        <p>Blood banking is the process that takes place in the lab to make sure that donated blood, or blood products, are safe before they are used in blood transfusions and other medical procedures. Blood banking includes typing the blood for transfusion and testing for infectious diseases.</p>
        <h2 class="h2">Facts about blood banking</h2>
        <p>According to the American Association of Blood Banks as of 2013:</p>
        <ul>
            <li>About 36,000 units of blood are needed every day. </li>
            <li>The number of blood units donated is about 13.6 million a year. </li>
            <li>About 6.8 million volunteers are blood donors each year</li>
            <li>Each unit of blood is broken down into components, such as red blood cells, plasma, cryoprecipitated AHF, and platelets. One unit of whole blood, once it's separated, may be transfused to several patients, each with different needs.</li>
            <li>Annually, more than 21 million blood components are transfused.</li>

        </ul>
        <h2 class="h2">Who are the blood donors?</h2>
        <p>Most blood donors are volunteers. However, sometimes, a patient may want to donate blood a couple of weeks before undergoing surgery, so that his or her blood is available in case of a blood transfusion. Donating blood for yourself is called an autologous donation. Volunteer blood donors must pass certain criteria, including the following:</p>
        <ul>
            <li>Must be at least 16 years of age, or in accordance with state law </li>
            <li>Must be in good health</li>
            <li>Must weigh at least 110 pounds</li>
            <li>Must pass the physical and health history exam given before donation</li>
        </ul>
        <p>Some states permit people younger than 16 or 17 years to donate blood, with parental consent.</p>

        <h2 class="h2">What are the blood types?</h2>
        <p>According to the American Association of Blood Banks, distribution of blood types in the U.S. includes the following:</p>
        <ul>
            <li>O Rh-positive - 39%</li>
            <li>A Rh-positive - 31%</li>
            <li>B Rh-positive - 9%</li>
            <li>O Rh-negative - 9%</li>
